public class Main {

    public static void main(String[] args) {
        System.out.println("******************************");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println();
        System.out.println("******************************");
        for (int i = 1; i < 10; i += 2) {
            System.out.println(i);
        }

        System.out.println();
        System.out.println("******************************");
        for (int i = 2; i < 10; i += 2) {
            System.out.println(i);
        }

    }
}
